<h1>Fan Reviews</h1>
<h2>Nap time</h2>
<h3>By: Charlie Gibbons</h3>
<p>-	This album really takes me back in time man. The first time I listened to this album I was sitting in the bed of my truck with my arm around my high school sweetheart. If only she was still in my arms… I don’t know what I did wrong but she just will not reply to my calls anymore. Nap Time really made me think about who I am as a person and who I want to be. It really touched me man. And whenever my girl told me to turn the music down I just turned it up louder because it was that good.</p>
<h2>Light and Stuff</h2>
<h3>By: Johnny Knoxville</h3>
<p>-	Wow what an album! From the ups and downs of the different songs to the magical voice of the head singer, this album really took things to the next level. I have heard rock before but this changed my perception of reality as a whole. One moment you are jamming to an upbeat tune and the next you are on the ground crying because it reminds you of your dead pet. Overall I would 100% recommend this album to anyone. My grandmother actually loves it and I think yours would too.</p>
<h2>Books</h2>
<h3>By: Gilbert Jackson</h3> 
<p>-	What a terrible surprise! I initially was looking for a book on tape and all of a sudden, I start hearing this music. Not the kind of music I usually listen to though… I prefer classical. This music gave me a massive migraine and I was actually hospitalized for a short amount of time after listening the album. In reality if I had known how to work my computer better I would have been able to turn the sound off. Unfortunately, I did not, and I was forced to listen to “Books” for an hour!!! Truly horrifying. I do not understand how any sane human being can listen to this and pretend like they enjoy it.</p>
<h2>Bottom Text</h2>
<h3>By: William Krzhizanovsky</h3>
<p>-	All I can say is that I am in complete awe. I have not heard music like this since Mozart. First off, the name is what really captured me. Bottom Text just makes me think of so many different things. I personally believe it refers to the writings of Satan. I have read the lyrics over the songs on this album and it enlightens me to a side of Satan I have never heard before. I have heard people interpret the album differently but that is why this album is golden. Anybody from any age group can just turn this album on and start jamming away.</p>
<h2>Fruit</h2>
<h3>By: Logan Kaminksi</h3>
<p>-	I have been a fan of this band for as long as they have been around. Fruit is such a lively bit of music that can turn a bad day into one you won’t want to forget. It reminds me a little bit of the early Beatles. If you have not already, I would say it is definitely worth a listen.


            
      
 
